package com.fight2.entity.engine;

public class SimplePoint {
    private float x;
    private float y;

    public float getX() {
        return x;
    }

    public void setX(final float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(final float y) {
        this.y = y;
    }

    public SimplePoint(final float x, final float y) {
        super();
        this.x = x;
        this.y = y;
    }
    
    
}
